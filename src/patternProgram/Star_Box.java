package patternProgram;

public class Star_Box {
	        //*****    //Rows=3
			//*****    //Colums=5
			//*****    //Always Strt With Rows(Outer Loop )
	public static void main(String[] args)
	{ // outer for loop for rowd (count rows) Here rows = 3
		for (int i=1; i<=4; i++)
		{
			//innorloop for columns(Count colums) Here Col =5
			for (int j=1; j<=5; j++)
			{
				System.out.print("*"); //Note :Use (Print) only
			}
			System.out.println();  //on next line use println
		}
		
		
		
		
	}

}
